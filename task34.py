# Задача 34:  Винни-Пух попросил Вас посмотреть, есть ли в его стихах ритм. 
# Поскольку разобраться в его кричалках не настолько просто, 
# насколько легко он их придумывает, Вам стоит написать программу. 
# Винни-Пух считает, что ритм есть, если число слогов (т.е. число гласных букв) 
# в каждой фразе стихотворения одинаковое. 
# Фраза может состоять из одного слова, если во фразе несколько слов, 
# то они разделяются дефисами. Фразы отделяются друг от друга пробелами. 
# Стихотворение  Винни-Пух вбивает в программу с клавиатуры. 
# В ответе напишите “Парам пам-пам”, если с ритмом все в порядке и “Пам парам”, 
# если с ритмом все не в порядке

def count_vowels(word):
    # Функция для подсчета гласных букв в слове
    vowels = "aeiouAEIOU"
    count = 0
    for letter in word:
        if letter in vowels:
            count += 1
    return count

def check_rhythm(pooh_poem):
    # Разделяем стихотворение на фразы по пробелам
    phrases = pooh_poem.split()

    # Подсчитываем количество слогов (гласных) в первой фразе
    first_phrase_syllables = count_vowels(phrases[0])

    # Проверяем остальные фразы на соответствие первой по количеству слогов
    for phrase in phrases[1:]:
        if count_vowels(phrase) != first_phrase_syllables:
            return "Пам парам"
    
    return "Парам пам-пам"

# Ввод стихотворения Винни-Пуха
pooh_poem = input("Введите стихотворение Винни-Пуха: ")

# Проверка наличия ритма и вывод результата
result = check_rhythm(pooh_poem)
print(result)


